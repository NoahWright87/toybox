// Fake filesystem for NS Doors 97

export type FsFileType = "text" | "exe" | "bat" | "sys" | "scr" | "drv" | "tmp" | "zip" | "bmp" | "ini";

export interface FsFile {
  kind: "file";
  name: string;
  type: FsFileType;
  content?: string;      // text files
  action?: string;       // exe: maps to a window/experience id, or "noop"
}

export interface FsFolder {
  kind: "folder";
  name: string;
  children: FsNode[];
}

export type FsNode = FsFile | FsFolder;

