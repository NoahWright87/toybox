// ── Helpers ───────────────────────────────────────────────────────────────────

export const FILE_TYPE_ICONS: Record<FsFileType | "folder", string> = {
  folder: "📁",
  text:   "📄",
  exe:    "⚙️",
  bat:    "📜",
  sys:    "🔧",
  scr:    "🖥️",
  drv:    "🔩",
  tmp:    "🗒️",
  zip:    "📦",
  bmp:    "🖼️",
  ini:    "📋",
};

export function getNodeIcon(node: FsNode): string {
  if (node.kind === "folder") return FILE_TYPE_ICONS.folder;
  return FILE_TYPE_ICONS[node.type] ?? "📄";
}

export function isReadable(node: FsFile): boolean {
  return node.content !== undefined;
}

// Build path string for a node given its ancestors
export function buildPath(ancestors: string[], name: string): string {
  if (ancestors.length === 0) return name;
  return ancestors.join("\\") + "\\" + name;
}
